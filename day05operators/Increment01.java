package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		
		// int data type'inda iki variable oluturun
		// Birinciyi 3 , ikinciyi 7 arttirin
		
		
		// Increment 1.YOL ---- Uzun yol
		
		int sayi1 = 12;
		sayi1 = sayi1 + 3;   // Increment ( sayiyi arttirma )
		System.out.println(sayi1);  // Ekranda 15 cikiyor
		
		
		int sayi2 = 12;
		sayi2 = sayi2 + 7;
		System.out.println(sayi2);

	}

}
