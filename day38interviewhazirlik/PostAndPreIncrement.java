package day38interviewhazirlik;

public class PostAndPreIncrement {

	public static void main(String[] args) {
		
		//post icrement
		
		int a =12;
		a=a++;
		System.out.println(a);//12
   
		//post icrement
		
		int b=12;
		b++;
		System.out.println(b);//13
		
		//pre icrement
		
		int c=12;
		c=++c;
		System.out.println(c);//13
		
	}

}
