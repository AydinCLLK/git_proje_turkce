package day05operators;

public class OperatorSigns {

	public static void main(String[] args) {

		boolean b1 = 12 == 20;
		System.out.println(b1); // Ekranda False cikar

		System.out.println(12 < 20); // Ekranda True cikar
		System.out.println(12 != 20); // ekranda true cikar
		System.out.println(12 >= 20); // ekranda false cikar
		System.out.println(12 <= 12); // ekranda true cikar

	}

}
