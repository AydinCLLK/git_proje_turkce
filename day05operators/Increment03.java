package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data type'inda iki tane variable olusturun
		// Bu variable'lari  EN KISA   yolu kullanarak 1 arttirin
		// EN KISA yolu sadece 1 arttirmak icin kullanilir.
		
		int sayi1 = 12;
		sayi1++;  // variable isminden sonra iki kere + sembolu yazarsaniz
			      // o variable'in degerini 1 arttirmis oluruz
		System.out.println(sayi1);
		
		
		int sayi2 = 20;
		sayi2++;
		System.out.println(sayi2);
		

	}

}
