package day05operators;

public class Increment02 {

	public static void main(String[] args) {

		// int data type'inda iki tane variable olusturun
		// Birinciyi 3 , ikinciyi 7 arttirin

		// Increment 2.YOL --- Kisa yol

		int sayi1 = 12;
		sayi1 += 3; // increment ==> kisa yol
		System.out.println(sayi1);

		int sayi2 = 12;
		sayi2 = sayi2 + 7; // 12 + 7 = 19 cikar
		sayi2 += 7; // 19 olan sayi2 ile 7 toplar 26 olur
		System.out.println(sayi2);

	}

}
